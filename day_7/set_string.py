my_set1 = {"apple", "banana", "cherry"}
my_set2 = {"banana", "kiwi", "mango"}
my_set_uninon = my_set1.union(my_set2)
my_set_intersection = my_set1.intersection(my_set2)
my_set_difference = my_set1.difference(my_set2)
my_set_symmetric_difference = my_set1.symmetric_difference(my_set2)
print("Union:", my_set_uninon)
print("Intersection:", my_set_intersection) 
print("Difference:", my_set_difference)
print("Symmetric Difference:", my_set_symmetric_difference)